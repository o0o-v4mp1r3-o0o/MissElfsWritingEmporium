package net.javaguides.springboot.account;


public class AccountObject {
	String username;
	int id;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public long getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
